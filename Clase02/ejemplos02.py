## EJEMPLOS PARA SALTO DE LINEA

# Nueva Línea (\n)

# Inserta un salto de línea en el texto impreso.

print("Hola\nMundo")

# Salida por consola: 
# Hola
# Mundo


# Tabulación (\t)

# Añade un conjunto fijo de espacios en blanco.

print("Nombre:\tJuan\nEdad:\t30")

# Salida por consola:

# Nombre: Juan

# Edad: 30

# End

# Añade una especificación que se incorpora al final de la cadena

print("Hola,", end=' ')
print("Mundo")

# Salida por consola:

# Hola, Mundo